package Dz.Auto;

public class Transport {
    int power;
    int max_speed;
    double weight;
    String car_model;

    public Transport(int power, int max_speed, double weight, String car_model) {
        this.power = power;
        this.max_speed = max_speed;
        this.weight = weight;
        this.car_model = car_model;
    }

}
